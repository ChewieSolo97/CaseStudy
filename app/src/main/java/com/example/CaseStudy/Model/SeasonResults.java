package com.example.CaseStudy.Model;

public class SeasonResults {

    private String year;
    private int rank;
    private String full_name;
    private int points;
    private int starts;
    private int wins;
    private int poles;
    private int top5;
    private int top10;
    private int dnf;
    private int laps_led;
    private double avg_start;
    private double avg_finish;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }

    public String getFull_name() {
        return full_name;
    }

    public void setFull_name(String full_name) {
        this.full_name = full_name;
    }

    public int getPoints() {
        return points;
    }

    public void setPoints(int points) {
        this.points = points;
    }

    public int getStarts() {
        return starts;
    }

    public void setStarts(int starts) {
        this.starts = starts;
    }

    public int getWins() {
        return wins;
    }

    public void setWins(int wins) {
        this.wins = wins;
    }

    public int getPoles() {
        return poles;
    }

    public void setPoles(int poles) {
        this.poles = poles;
    }

    public int getTop5() {
        return top5;
    }

    public void setTop5(int top5) {
        this.top5 = top5;
    }

    public int getTop10() {
        return top10;
    }

    public void setTop10(int top10) {
        this.top10 = top10;
    }

    public int getDnf() {
        return dnf;
    }

    public void setDnf(int dnf) {
        this.dnf = dnf;
    }

    public int getLaps_led() {
        return laps_led;
    }

    public void setLaps_led(int laps_led) {
        this.laps_led = laps_led;
    }

    public double getAvg_start() {
        return avg_start;
    }

    public void setAvg_start(double avg_start) {
        this.avg_start = avg_start;
    }

    public double getAvg_finish() {
        return avg_finish;
    }

    public void setAvg_finish(double avg_finish) {
        this.avg_finish = avg_finish;
    }
}
